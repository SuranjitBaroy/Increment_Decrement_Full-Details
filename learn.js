
//Post Increment
var a = 10;

var result = a++;

console.log(a);
console.log(result);

//Pre Increment
var a = 10;

var result = ++a;

console.log(a);
console.log(result);


//Post Decrement

var a = 10;

var result = a--;

console.log(a);
console.log(result);


//Pre Decrement

var a = 10;

var result = --a;

console.log(a);
console.log(result);

